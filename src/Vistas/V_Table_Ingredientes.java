    public static void modificaFilaIngredientes(Object[] dataFila){
        DefaultTableModel model = (DefaultTableModel) jTableIngredientes.getModel();
        //model.setValueAt((Integer.parseInt((String) dataFila[0])), indiceIngrediente, 0); 
        model.setValueAt(((String) dataFila[1]), indiceIngrediente, 1); 
        model.setValueAt((Integer.parseInt((String) dataFila[2])), indiceIngrediente, 2); 

        // Notifica a la tabla que la fila ha sido modificada
        model.fireTableRowsUpdated(indiceIngrediente, indiceIngrediente);
        
        
    }
    
    public static void agregaFilaIngredientes(Object[] dataFila){
        DefaultTableModel model = (DefaultTableModel) jTableIngredientes.getModel();
        model.addRow(dataFila);
    }
    
    public void cargaTablaIngredientes() {
        DefaultTableModel datos = (DefaultTableModel) jTableIngredientes.getModel();
        datos.setNumRows(0);
        IngredienteData id = new IngredienteData();
        id.litarIngredientes().forEach((Ingrediente i) -> {
            Object[] fila = new Object[3];
            fila[0] = i.getIdIngrediente();
            fila[1] = i.getNombre();
            fila[2] = i.getCaloriasPor100GR();
            datos.addRow(fila);
        });
        jTextBuscarIngrediente.setText("");
    }
    
    private void borrarIngredientes() {
        int f = modeloIngre.getRowCount() - 1;
        for (; f >= 0; f--) {
            modeloIngre.removeRow(f);
        }
    }

    private void borrarFilas() {
        for (int f = jTableIngredientes.getRowCount() - 1; f >= 0; f--) {
            modeloIngre.removeRow(f);
        }
    }
    
        

    
