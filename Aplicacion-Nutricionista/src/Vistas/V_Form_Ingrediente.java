    public void agregaIngrediente() {
        if (!validaDatos()) {
            IngredienteData ingreData = new IngredienteData();
            Object[] objeto = new Object[]{
                jTidIngrediente.getText(),
                jTnombre.getText(),
                jTcalorias.getText()
            };
            
            //Agrega ingrediente a base de datos
            int id = ingreData.insertIngrediente(objeto);
            
            objeto[0] = id;
            //Agrega ingrediente a tabla de vista Ingredientes
            V_Table_Ingredientes.agregaFilaIngredientes(objeto);;
            dispose();
        }
    }

    public void modificaIngrediente() {
        if (!validaDatos()) {
            //Modifica registro en base de datos
            Ingrediente ingre = new Ingrediente(Integer.valueOf(jTidIngrediente.getText()), 
                    jTnombre.getText(), Integer.valueOf(jTcalorias.getText()));
            IngredienteData ingreData = new IngredienteData();
            ingreData.modificarIngrediente(ingre);
            
            //Modifica registro en Tabla Comida de V_Table_AdminComidas
            V_Table_Ingredientes.modificaFilaIngredientes(new Object[]{
                idIngre, 
                jTnombre.getText(), 
                jTcalorias.getText()
            });
            
            dispose();
        } else {
        //    JOptionPane.showMessageDialog(this, "Verifique los datos", "ERROR", JOptionPane.ERROR_MESSAGE);
        }
    }
    
    //VALIDACIONES
    public boolean validaDatos(){
        int nro; 
        if (jTcalorias.getText().isEmpty()) {
            nro = 0;
            jTcalorias.setText("0");
        } else{
            nro = Integer.parseInt(jTcalorias.getText());
        }
        
        if (jTnombre.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Ingrese todos los datos solicitados", "ERROR", JOptionPane.ERROR_MESSAGE);
            return true;
        } else if (nro == 0 || nro > 5000) {
            JOptionPane.showMessageDialog(this, "Caloria fuera de rango...Verifique los datos", "ERROR", JOptionPane.ERROR_MESSAGE);
            jTcalorias.setText("0");
            return true;    
        } else {
            return false; 
        }
    }
    
    public void validaNumero(java.awt.event.KeyEvent evt) {
        final char tecla = evt.getKeyChar();
        if (Character.isDigit(tecla)
                || tecla == KeyEvent.VK_BACKSPACE
                || tecla == KeyEvent.VK_DELETE
                || (evt.isControlDown() && evt.getKeyCode() == KeyEvent.VK_V)) {
        } else {
            evt.consume();
        }
    }
    
