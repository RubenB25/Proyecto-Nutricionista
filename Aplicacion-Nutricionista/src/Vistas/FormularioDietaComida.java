    private void llenarTablaComida() {
 
        DefaultTableModel datos = (DefaultTableModel) jTcomida.getModel();
        datos.setNumRows(0);
        ComidaData cd = new ComidaData();
        cd.listarComidas().forEach((c) -> {
            Object[] fila = new Object[3];
            fila[0] = c.getNombre();
            fila[1] = c.getDetalle();
            fila[2] = c.getCantCalorias();
            datos.addRow(fila);
        });
    }
